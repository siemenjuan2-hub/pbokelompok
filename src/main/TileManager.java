    // DRAW MAP
    public void draw(Graphics2D g2) {
        // Menggunakan variabel global GamePanel agar tidak terjadi Hardcode angka loop
        for (int row = 0; row < gp.maxScreenRow; row++) {
            for (int col = 0; col < gp.maxScreenCol; col++) {

                int tileNum = mapTileNum[row][col];

                int x = col * gp.tileSize;
                int y = row * gp.tileSize;

                // Memastikan tile tidak null sebelum digambar untuk menghindari NullPointerException
                if (tile[tileNum] != null) {
                    g2.drawImage(tile[tileNum], x, y, gp.tileSize, gp.tileSize, null);
                }
            }
        }
    }
